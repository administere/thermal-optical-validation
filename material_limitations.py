# ============================================================
# 局限性 4: 晶体结构不确定度
# ============================================================
def limitation_4_structure():
    """C70 共晶晶体学数据质量问题"""
    print(f"""
{'='*60}
  局限性 4: C70 共晶的晶体学数据不确定度高
{'='*60}

  R 因子对比 (MOESM1 Table S2):
    2DiSubPc 纯相:   R = 6.18%   → 结构确定
    2DiSubPc-C60:    R = 13.35%  → 可接受
    2DiSubPc-C70:    R = 23.67%  → 不确定度高

  R > 20% 在有机共晶中常见但意味着:
    → 精确的原子位置不确定
    → C70 的取向可能是无序的
    → 空隙中的残余电子密度未完全建模
    → 极性空间群 (Cc) 的结论是可靠的
    → 但精确的分子间距离 (±0.05Å) 和角度 (±1°) 不确定
""")
    return {}


# ============================================================
# 核心分析: 外部加热至 242°C
# ============================================================
def external_heating_analysis():
    """外部烘箱加热 vs 自加热"""
    print(f"""
{'='*60}
  外部加热至 242°C — 一个更优的实验路径
{'='*60}
""")

    # 参数
    alpha_850 = 350       # cm⁻¹ (乌尔巴赫外推)
    alpha_570 = 50000     # cm⁻¹ (带隙共振, 典型有机半导体)
    dn_dT = -1e-4         # /K
    Delta_T = 215         # K

    # ── 自加热方案 (当前) ──
    film_self = 10.0      # μm
    lam_self = 850        # nm
    abs_self = 1 - np.exp(-alpha_850 * film_self * 1e-4)
    dphi_self = 2 * np.pi / (lam_self * 1e-9) * abs(dn_dT) * Delta_T * film_self * 1e-6
    P_vcsel_self = 5.0    # mW

    # ── 外部加热方案 ──
    film_ext = 2.0        # μm (只需调制相位, 不需要吸收能量)
    lam_ext = 570         # nm (带隙共振, α 大 ~100×)
    abs_ext = 1 - np.exp(-alpha_570 * film_ext * 1e-4)
    dphi_ext = 2 * np.pi / (lam_ext * 1e-9) * abs(dn_dT) * Delta_T * film_ext * 1e-6
    P_vcsel_ext = 0.5     # mW (只负责信号, 不负责加热)
    P_oven = 50           # W (小型真空烘箱)

    contrast_self = (1 - np.exp(-alpha_850 * 10e-4 * (1 - 0.01)))
    contrast_ext = (1 - np.exp(-alpha_570 * 2e-4 * (1 - 0.01)))

    print(f"""
  方案 A: 自加热 (当前设计)          方案 B: 外部烘箱加热
  ────────────────────────────────────────────────────────────
  加热来源           VCSEL 光热吸收               真空烘箱 (均匀 PID)
  温度均匀性         有梯度 (~10°C 中心→边缘)     ±1°C
  VCSEL 波长         850nm (固定)                 570nm (自由选择!)
  为什么是这个波长    需要低光子能量来加热         选择带隙共振波长
  薄膜厚度           10μm                         2μm
  吸收率             {abs_self*100:.0f}%                          {abs_ext*100:.0f}%
  Δφ (调制深度)      {dphi_self:.1f} rad = {dphi_self/np.pi:.1f}π            {dphi_ext:.1f} rad = {dphi_ext/np.pi:.2f}π
  VCSEL 功率/像素    {P_vcsel_self:.0f} mW                           {P_vcsel_ext:.1f} mW
  烘箱功率           —                             ~{P_oven} W
  系统复杂度         高 (每像素功率平衡)          低 (标准实验室设备)
  实验可行性         ⚠️ 需要精确的多像素控制       ✅ 立即可以开始

  自加热方案的核心矛盾:
  ┌──────────────────────────────────────────────────────────┐
  │  850nm: VCSEL 技术成熟, 但材料吸收极弱 (~30%)          │
  │  570nm: 材料吸收极强 (~100%), 但光子能量高 2×           │
  │         → 自加热效率更低 (单个光子产热更多但总数少)     │
  │                                                          │
  │  外部加热消除了这个矛盾:                                │
  │  用烘箱加热, 用最优波长计算 — 各司其职                 │
  └──────────────────────────────────────────────────────────┘
""")

    print(f"""
{'='*60}
  建议: 两步实验路线
{'='*60}

  第一步 (近期, ~$5K, 2周): 外部加热验证核心物理
    1. 小型真空烘箱 + DiSubPc·C70 薄膜
    2. 570nm 激光测透射率 vs 温度 (27°C → 242°C)
    3. 直接测量 Δn(T) — 验证热光系数
    4. 确认 242°C 下材料稳定性 (MOESM7 已提供证据)

  第二步 (中期): 验证光学调制
    1. 在 242°C 外部加热下, 用第二束光做泵浦-探测
    2. 直接测量调制深度 vs 泵浦功率
    3. 量化饱和吸收阈值
    4. 测量调制带宽 (频率扫描)

  第三步 (远期): 自加热集成
    1. 基于前两步的实测参数, 重新设计 VCSEL 功率预算
    2. 验证自加热的可行性 (或证明外部加热是更优方案)
    3. 集成到完整的处理器原型

  为什么外部加热优先:
    → 解耦变量: 先单独验证材料物理, 再考虑系统集成
    → 降低风险: 自加热涉及光-热-电多场耦合, 问题难以隔离
    → 标准设备: 真空烘箱是实验室标准配置, 不需要定制硬件
    → 更快出结果: 2周 vs 2个月

  自加热仍然是终极目标:
    优雅性: 光同时做计算和加热 — 没有比这更简洁的设计
    但优雅不等于优先: 先用简单方法验证物理, 再做工程优化.
""")

    return {
        'self': {'film_um': film_self, 'lam_nm': lam_self, 'abs': abs_self,
                 'dphi_rad': dphi_self, 'P_vcsel_mW': P_vcsel_self},
        'external': {'film_um': film_ext, 'lam_nm': lam_ext, 'abs': abs_ext,
                     'dphi_rad': dphi_ext, 'P_vcsel_mW': P_vcsel_ext, 'P_oven_W': P_oven},
    }


if __name__ == '__main__':
    limitation_1_bandgap()
    limitation_2_lifetime()
    limitation_3_missing_data()
    limitation_4_structure()
    results = external_heating_analysis()
