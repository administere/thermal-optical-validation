#!/usr/bin/env python3
"""
DiSubPc·C70 材料局限性 + 外部加热方案分析
===========================================

回答两个核心问题:
  1. 这个材料有什么局限性?
  2. 如果外部加热到 242°C (不用自加热), 性能如何?

结论:
  外部加热至 242°C + 570nm VCSEL 是更优的第一实验路径.
  自加热是远期目标 (优雅但复杂), 外部加热是近期实验 (简单可控).
"""

import numpy as np

# ============================================================
# 局限性 1: 850nm 是带隙以下吸收
# ============================================================
def limitation_1_bandgap():
    """光学带隙导致的根本限制"""
    E_g = 2.25  # eV (MOESM1 Table S1)
    lam_onset = 1240 / E_g  # nm

    lam_vcsel = 850  # nm
    E_vcsel = 1240 / lam_vcsel
    delta_E = E_g - E_vcsel

    # MOESM6 消光系数对比
    eps_300nm = 69700   # L/(cm·mol) — 带隙以上
    eps_800nm = 300     # L/(cm·mol) — 带隙以下

    print(f"""
{'='*60}
  局限性 1: 850nm 是带隙以下吸收 — 物理极限, 不是工程问题
{'='*60}

  光学带隙 E_g = {E_g} eV → 吸收起始 λ ≈ {lam_onset:.0f} nm
  VCSEL @ {lam_vcsel}nm = {E_vcsel:.2f} eV → 低于带隙 {delta_E:.2f} eV

  消光系数对比 (MOESM6 Fig 2b):
    ε(300nm) = {eps_300nm:,} L/(cm·mol)  ← 带隙以上, 强吸收
    ε(800nm) = {eps_800nm} L/(cm·mol)     ← 带隙以下, 弱吸收
    比值: {eps_300nm/eps_800nm:.0f}×  (差了 230 倍)

  850nm 处的吸收来自:
    → 电荷转移 (CT) 带尾 (弱)
    → 乌尔巴赫带尾 (弱)
    → 不是直接 HOMO→LUMO 跃迁 (那在 551nm)

  结论: 选择 850nm VCSEL 等于选择在材料最弱的波段工作.
        这不是工程优化能解决的 — 是材料自身的物理极限.
""")

    return {'E_g': E_g, 'lam_onset': lam_onset, 'eps_ratio': eps_300nm/eps_800nm}


# ============================================================
# 局限性 2: 激发态寿命 vs 调制速度
# ============================================================
def limitation_2_lifetime():
    """激发态衰减时间限制"""
    tau_decay_300K = 4.2   # ns (MOESM8 Fig 5d)
    f_max_300K = 1 / (2 * np.pi * tau_decay_300K * 1e-9) * 1e-9  # GHz

    # MOESM1 Table S3: τ₂ 随温度升高反而增长
    temps_K = np.array([6, 100, 200, 300])
    tau2_vals = np.array([3.72, 4.20, 4.62, 4.96])  # ns

    slope = np.polyfit(temps_K, tau2_vals, 1)[0]
    tau2_515K = np.polyval(np.polyfit(temps_K, tau2_vals, 1), 515)
    f_max_515K = 1 / (2 * np.pi * tau2_515K * 1e-9) * 1e-9

    print(f"""
{'='*60}
  局限性 2: 激发态寿命限制了经典调制速度
{'='*60}

  激发态衰减 τ_decay ≈ {tau_decay_300K} ns @ 300K (MOESM8 Fig 5d)
  → 经典调制带宽 ≈ 1/(2πτ) ≈ {f_max_300K:.2f} GHz

  PL τ₂ 温度依赖性 (MOESM1 Table S3):
    6K:   3.72 ns
    100K: 4.20 ns
    200K: 4.62 ns
    300K: 4.96 ns
    515K: ~{tau2_515K:.1f} ns (外推)

  ⚠️ τ₂ 随温度升高反而增长 — 热激活延长了激发态寿命
  → 在 242°C 时调制带宽反而更低: ~{f_max_515K:.2f} GHz

  17.6 GHz 量子拍频 vs 经典调制:
    量子拍频: S₁↔¹TT 相干布居振荡 — 不是透射率调制
    经典调制: 需要 Δn → 透射率变化 → 受限于激发态衰减
    两者不矛盾, 但 17.6 GHz 不能直接用作调制频率
""")

    return {'tau_decay_ns': tau_decay_300K, 'tau2_515K_ns': tau2_515K,
            'f_max_GHz': f_max_300K, 'f_max_515K_GHz': f_max_515K}


